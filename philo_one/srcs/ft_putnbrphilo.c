#include <unistd.h>
#include "ft_philo.h"

void
	ft_putnbrphilo(int n)
{
	char	nb[11];
	int		i;

	i = 9;
	nb[10] = ' ';
	while (n > 9)
	{
		nb[i] = '0' + n % 10;
		n = n / 10;
		i--;
	}
	nb[i] = '0' + n % 10;
	write(1, &nb[i], 11 - i);
}
